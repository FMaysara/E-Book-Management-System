<h2>Book Summary</h2><br>
<p>It Ends with Us by Colleen Hoover has to be one of my favorite romance books of all time. It Ends with Us is a book that follows a girl named Lily who has just moved and is ready to start her life after college. Lily then meets a guy named Ryle and she falls for him. As she is developing feelings for Ryle, Atlas, her first love, reappears and challenges the relationship between Lily and Ryle. After reading the book, I rate It Ends with Us 5 stars. This book is a 5-star worthy book because it did not fall into the same repetitive trap as other romance books commonly do. I have noticed from reading lots of romance books that there is the same basic plot in almost every single one, and this book did not have that, which was really refreshing. Something that I really appreciated within this book was the powerful and impactful message that is normally not discussed. I did not expect to be impacted and touched by the book as much as I did. I also really loved how Lily’s character was portrayed in the book. The amount of strength that Lily shows throughout the book is truly inspiring. This story is told only from Lily’s perspective and I loved this aspect. It Ends with Us really allows you to experience and feel everything that Lily goes through and it makes for a gut-wrenching book that will make you laugh, smile, and cry. Lastly, this book gets 5 stars from me because of how complex and deep the plot and the characters were. The story dives in and explores the grey area of things and shows how life is not purely black and white. The book was constantly surprising me and I never wanted to put the book down. Overall, this book is not only one of the best romance books that I have read, but one of the best books I have ever read in general. </p>
